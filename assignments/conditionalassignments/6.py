# Write a program to take a single digit number from the keyboard and print in english?
a=int(input("enter a single digit number:"))
if a==1:
    print("ONE")
elif a==2:
    print("TWO")
elif a==3:
    print("THREE")
elif a==4:
    print("FOUR")
elif a==5:
    print("FIVE")
elif a==6:
    print("SIX")
elif a==7:
    print("SEVEN")
elif a==8:
    print("EIGHT")
elif a==9:
    print("NINE")
elif a==0:
    print("ZERO")
else:
    print("enter only single DIGIT and POSITIVE NUMBER")